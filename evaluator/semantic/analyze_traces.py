def main() -> int:
    parser = argparse.ArgumentParser(description="Analyze BinBench semantic traces")
    parser.add_argument("output_dir", nargs="?", help="Directory containing semantic outputs")
    args = parser.parse_args()

    base_dir = args.output_dir or os.path.abspath(os.path.join(os.path.dirname(__file__), "../../results/semantic"))

    trace_orig_path = os.path.join(base_dir, "trace_original.txt")
    trace_decomp_path = os.path.join(base_dir, "trace_decompiled.txt")
    report_path = os.path.join(base_dir, "result_analysis.md")
    metrics_path = os.path.join(base_dir, "result_metrics.json")

    if not os.path.exists(trace_orig_path) or not os.path.exists(trace_decomp_path):
        print(f"Error: Trace files not found in {base_dir}")
        return 1

    analyzer = SemanticAnalyzer(base_dir)
    metrics, report = analyzer.analyze()

    atomic_write_text(report_path, report)
    atomic_write_json(metrics_path, metrics)

    print(f"Analysis complete. Report generated at {report_path}")
    print(f"Metrics generated at {metrics_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
